            <h2 class="text-center mt-4">THE REALITY OF BASIC SANITATION IN BRAZIL</h2>
            <div class="mt-4 mb-5">
                <p> In January 2017, Instituto Trata Brasil released a documentary to talk about the reality of basic sanitation in Brazil. In the report, residents and sanitation experts are interviewed.</p>
                <p>Residents blame the government in the midst of terrible sanitation situations. The reality is also demonstrated by the insufficient numbers, mainly for sewage collection and treatment.</p>
                <p>Surveys carried out in 2016 by the National Sanitation Information System (SNIS) show that more than 51.9% of the Brazilian population does not have access to sewage collection.</p>
                <p>Some works were carried out in the country in terms of infrastructure, but sanitation was forgotten as it was not a work of public prestige. According to one of the interviewees, investing in sanitation was seen as “burying money” by some government officials.</p>
                <p>According to Anthony Wong, a toxicologist and pediatrician, the consequences of ignoring sanitation in the country mainly affect the poorest. However, it can reach the higher classes such as the Dengue or Zika epidemics.</p>
                <p>Population growth also highlighted the cities' lack of planning in relation to basic sanitation, as the article highlights.</p>
                <p>The Attorney of Justice, Sávio Bittencourt, recalls that taking care of basic sanitation is essential for the preservation of natural resources and for public health. Other factors such as economic growth, human and technological development are also consequences of actions in this regard.</p>
                <p> Even with the need of the environment and the population, the sanitation numbers walk at a slow pace. </p>
                <p> Below, we will see a little about the reality of basic sanitation in Brazil in each of its aspects.</p>
